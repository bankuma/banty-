		<h1 style="color:blue;"><center ><em><u>TIP OF A SUCCESSFUL DONATION</u></em></em></center></h1>
		<font color="maroon"><h3>By following a few recommendations before, during and after your blood donation can help you make your donation experience as safe, successful and pleasant as possible.</h3>
		<img src="bl.jpg" style="width:1340px;height:500px;">
		<font color="blue"><h3><u><em>Before Your Donation</em></u></h3>
		<h4><ul type="square">
		<li>Eat HealthyMaintain a healthy iron level in your diet by eating iron rich foods, such as red meat, fish, poultry, beans, spinach, iron-fortified cereals and raisins.
</li>
		<li>Get a good night's sleep.</li>
		<li>Drink an extra 16 oz. of water or nonalcoholic fluids before the donation.</li>
		<li>Eat a healthy meal before your donation. Avoid fatty foods, such as hamburgers, fries or ice cream before donating. (Fatty foods can affect the tests we do on your blood. If there is too much fat in your blood, your donation cannot be tested for infectious diseases and the blood will not be used for transfusion.)</li>
		<li>If you are a platelet donor, remember that your system must be free of aspirin for two days prior to donation.</li>
		</ul></h4>
		<img src="meat.jpg" style="width:265px;height:220px;">
